    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Date date = btnCaducidad.getDate();
        long f = date.getTime();
        java.sql.Date fecha = new java.sql.Date(f);
        int idFarmacia = 0;

        for (int i = 0; i < new Farmacia().GetAllModel().getRowCount(); i++) {
            if (new Farmacia().GetAllModel().getValueAt(i, 1).equals(cbIdFarmacia.getItemAt(cbIdFarmacia.getSelectedIndex()))) {
                idFarmacia = (int) new Farmacia().GetAllModel().getValueAt(i, 0);
            }
        }
        if (producto.getIdProducto() > 0) {
            producto.setNombre(txtNombre.getText());
            producto.setCaducidad("" + fecha);
            producto.setStock((Integer.parseInt(txtStock.getText())));
            producto.setIdFarmacia(idFarmacia);
            producto.setActivo(1);
            if (producto.Update()) {
                JOptionPane.showMessageDialog(null, "Producto actualizado con éxito");
                this.dispose();
            }
        } else {
            if (new Producto(0,
                    txtNombre.getText(),
                    "" + fecha,
                    Integer.parseInt(txtStock.getText()),
                    idFarmacia,
                    1).Add()) {
                JOptionPane.showMessageDialog(null, "Producto guardado con éxito");
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

