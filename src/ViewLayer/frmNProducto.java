    public frmNProducto(int idProducto) {
        initComponents();
        for (int i = 0; i < new Farmacia().GetAllModel().getRowCount(); i++) {
            cbIdFarmacia.addItem((String) new Farmacia().GetAllModel().getValueAt(i, 1));
        }

        this.idProducto = idProducto;
        producto.setIdProducto(idProducto);
        producto.GetById();
        txtNombre.setText(producto.getNombre());
        try {
            DateFormat fecha = new SimpleDateFormat("yyyy-MM-dd");
            Date cambiar = fecha.parse(producto.getCaducidad());
            btnCaducidad.setDate(cambiar);
        } catch (Exception e) {

        }
        txtStock.setText("" + producto.getStock());
        for (int i = 0; i < new Farmacia().GetAllModel().getRowCount(); i++) {
            if (new Farmacia().GetAllModel().getValueAt(i, 0).equals(producto.getIdFarmacia())) {
                cbIdFarmacia.setSelectedIndex(i);
            }
        }
    }

