    public void ControlaInstancia(JInternalFrame inter) {

        boolean mostrar = true;
        for (int i = 0; i < desktopPanel.getComponentCount(); i++) {
            if (inter.getClass().isInstance(desktopPanel.getComponent(i))) {
                inter.toFront();
                desktopPanel.moveToFront(inter);
                mostrar = false;

            } else {

            }
        }
        if (mostrar) {
            desktopPanel.add(inter);
            Dimension destopSize = desktopPanel.getSize();
            Dimension frame = inter.getSize();
            inter.setLocation((destopSize.width - frame.width) / 2, (destopSize.height - frame.height) / 2);
        }
        inter.show();

    }

